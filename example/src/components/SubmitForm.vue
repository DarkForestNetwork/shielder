<template>
  <div>
    <div class="field">
      <label class="label">Private Key</label>
      <div class="control">
        <input
          class="input"
          type="text"
          placeholder="0x..."
          v-model="privateKey"
        />
      </div>
      <p v-if="!privateKeyValid" class="help is-danger">Invalid private key</p>
      <p v-else class="help is-success">&check;</p>
    </div>

    <div class="field">
      <label class="label">Message</label>
      <div class="control">
        <input
          class="input"
          type="text"
          placeholder="Message"
          v-model="message"
        />
      </div>
    </div>

    <div class="field is-grouped">
      <div class="control">
        <button
          class="button is-primary"
          :class="{ 'is-loading': waitingForTx }"
          v-on:click="onSend(0)"
          :disabled="sendDisabled"
        >
          Send Encrypted
        </button>
      </div>
      <div class="control">
        <button
          class="button is-primary"
          :class="{ 'is-loading': waitingForTx }"
          v-on:click="onSend(1)"
          :disabled="sendDisabled"
        >
          Send Unencrypted
        </button>
      </div>
    </div>
  </div>
</template>

<script>
import { ethers } from "ethers";
import {
  getBatchIndexAtBlock,
  getRandomNonce,
  encodeMessage,
  encryptMessage,
} from "../utils.js";
import { getBlockNumber } from "../blocknumber.js";

export default {
  name: "SubmitForm",
  props: ["config", "eonKey"],

  data() {
    return {
      batcherContract: null,

      message: "",
      privateKey:
        "0xaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa",
      waitingForTx: false,
    };
  },

  computed: {
    sendDisabled() {
      return (
        this.waitingForTx ||
        this.eonKey === null ||
        this.config === null ||
        !this.privateKeyValid
      );
    },
    privateKeyValid() {
      if (!ethers.utils.isHexString(this.privateKey, 32)) {
        return false;
      }
      try {
        new ethers.Wallet(this.privateKey);
      } catch {
        return false;
      }
      return true;
    },
    address() {
      if (!this.privateKeyValid) {
        return null;
      }
      return ethers.utils.computeAddress(this.privateKey);
    },
  },

  mounted() {
    let signer = this.$provider.getSigner();
    this.configContract = this.$configContract.connect(signer);
    this.batcherContract = this.$batcherContract.connect(signer);
  },

  methods: {
    async onSend(type) {
      if (this.sendDisabled) {
        return;
      }
      this.waitingForTx = true;
      try {
        await this.sendTransaction(type);
      } finally {
        this.waitingForTx = false;
      }
    },

    async sendTransaction(type) {
      let nonce = getRandomNonce();
      let encodedMessage = await encodeMessage(
        this.message,
        nonce,
        this.privateKey
      );

      let blockNumber = await this.waitForGoodBlock();
      let batchIndex = getBatchIndexAtBlock(blockNumber + 1, this.config);

      if (type == 0) {
        encodedMessage = await encryptMessage(
          encodedMessage,
          this.eonKey,
          batchIndex
        );
      }

      let tx = await this.batcherContract.addTransaction(
        batchIndex,
        type,
        encodedMessage,
        {
          gasLimit: 200000,
        }
      );
      console.log(
        "tx",
        tx.hash,
        "sent for batch",
        batchIndex.toString(),
        "in block",
        blockNumber.toString()
      );
    },

    async waitForGoodBlock() {
      for (;;) {
        const blockNumber = await getBlockNumber(this.$provider);
        const batchIndexNow = getBatchIndexAtBlock(
          blockNumber + 1,
          this.config
        );
        const batchIndexSoon = getBatchIndexAtBlock(
          blockNumber + 2,
          this.config
        );
        if (batchIndexNow.eq(batchIndexSoon)) {
          return blockNumber;
        }
        await new Promise((resolve) => setTimeout(resolve, 1000));
      }
    },
  },
};
</script>

